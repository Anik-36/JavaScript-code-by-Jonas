// Mask Credit card

const maskCredit = function (number) {
  const str = number + '';
  const last = str.slice(-4);
  return last.padStart(str.length, '*');
};
console.log(maskCredit(38475050));
console.log(maskCredit(3847438203489238));
console.log(maskCredit('3928348992578257982'));

